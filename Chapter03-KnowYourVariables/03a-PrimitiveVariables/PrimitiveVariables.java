public class PrimitiveVariables {
    public static void main(String[] args) {

	// Small integer (-128 to 127) - 8bit
        byte b = 100;

	// Larger than byte (-32k to 32k) - 16bit
	short s = 1000;

	//Standard integer type - 32bit
	int x = 42; 

	// Big integers - 64bit
	long l = 900000L;

	// Single-precision decimal - 32bit
	float f = 3.14f;

	// Double-precision decimal - 64bit
	double d = 2.718;

	// Single Unicode character - 16bit
	char c = 'A';

	// Logical value (`true` or `false`) - 1bit
	boolean fa = false;
    }
}