public class Statements {
	public static void main (String[] args) {
		// integer assignment
		int x = 3;
		System.out.print("x is still " + x);

		// string assignment
		String name = "Dirk";
		System.out.print("the name is" + name);

		// multiplication
		x = x * 17;
		System.out.print("x * 17 is " + x);

		// random number
		double d = Math.random();
		System.out.print("Random number: " + d);

	}
}